public class Student
{
    public string id;
    public string name;
    public int age;
    public double gpa;
    public Student() { }

    public Student(string id, string name, int age, double gpa)
    {
        this.id = id;
        this.name = name;
        this.age = age;
        this.gpa = gpa;
    }

     
}

